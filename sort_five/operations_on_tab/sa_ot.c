#include "../../header.h"

int	*sa_ot(int *tab, int size)
{
	int	tmp;

	if (!tab || size < 2)
		return (NULL);
	else
	{
		tmp = tab[0];
		tab[0] = tab[1];
		tab[1] = tmp;
	}
	return (tab);
}
